# Infer the container port for a compose service from resolved-config.yaml.
# Requires yq.
#
# Usage:
#   port=$(__xcind-proxy-infer-port "web" /path/to/resolved-config.yaml)
__xcind-proxy-infer-port() {
  local service="$1"
  local resolved_config="$2"

  local port_count
  port_count=$(yq ".services.\"$service\".ports | length" "$resolved_config" 2>/dev/null) || port_count=0

  if [ "$port_count" -eq 0 ]; then
    echo "Error: Service '$service' has no port mappings. Specify port explicitly." >&2
    return 1
  elif [ "$port_count" -gt 1 ]; then
    echo "Error: Service '$service' has multiple port mappings. Specify port explicitly." >&2
    return 1
  fi

  # Extract the target (container) port from the single port mapping
  local target
  target=$(yq ".services.\"$service\".ports[0].target" "$resolved_config" 2>/dev/null)

  if [ -z "$target" ] || [ "$target" = "null" ]; then
    # Try published format (string like "80:8080")
    local port_str
    port_str=$(yq ".services.\"$service\".ports[0]" "$resolved_config" 2>/dev/null)
    if [[ $port_str == *:* ]]; then
      target="${port_str##*:}"
    else
      target="$port_str"
    fi
  fi

  echo "$target"
}

# --------------------------------------------------------------------------
# Service Validation
# --------------------------------------------------------------------------

# Check that a compose service exists in resolved-config.yaml.
__xcind-proxy-validate-service() {
  local service="$1"
  local resolved_config="$2"

  local exists
  exists=$(yq ".services | has(\"$service\")" "$resolved_config" 2>/dev/null)

  if [ "$exists" != "true" ]; then
    local available
    available=$(__xcind-list-services "$resolved_config" | tr '\n' ', ' | sed 's/,$//')
    echo "Error: Service '$service' not found in compose config. Available services: $available" >&2
    return 1
  fi
}

# --------------------------------------------------------------------------
# Hook Function
# --------------------------------------------------------------------------

# Resolve the effective TLS mode for a single export, given the current
# proxy-level TLS setting. Sets _effective_tls to one of:
#   both    — emit HTTP + HTTPS routers
#   https   — emit HTTPS router; HTTP router is a redirect to HTTPS
#   http    — emit HTTP router only (no HTTPS)
#
# Inputs: _tls (export-level), $1 (proxy-level TLS mode).
__xcind-proxy-resolve-export-tls() {
  local proxy_tls="$1"
  if [[ $proxy_tls == "disabled" ]]; then
    _effective_tls="http"
    return 0
  fi
  case "$_tls" in
  disable) _effective_tls="http" ;;
  require) _effective_tls="https" ;;
  auto | *) _effective_tls="both" ;;
  esac
}

# Main hook function called by the xcind pipeline on cache miss.
# Reads pipeline env vars and generates compose.proxy.yaml.
xcind-proxy-hook() {
  local app_root="$1"

  # Check if exports are defined (guard against unset under set -u)
  if [[ -z ${XCIND_PROXY_EXPORTS+set} || ${#XCIND_PROXY_EXPORTS[@]} -eq 0 ]]; then
    return 0
  fi

  # Default domain and source global config
  XCIND_PROXY_DOMAIN="${XCIND_PROXY_DOMAIN:-localhost}"
  local global_config="${XCIND_PROXY_CONFIG_DIR}/config.sh"
  if [ -f "$global_config" ]; then
    # shellcheck disable=SC1090
    source "$global_config"
  fi

  local proxy_tls_mode="${XCIND_PROXY_TLS_MODE:-auto}"

  # Require yq
  if ! command -v yq &>/dev/null; then
    echo "Error: yq is required for proxy hook but was not found." >&2
    return 1
  fi

  local resolved_config="$XCIND_CACHE_DIR/resolved-config.yaml"

  local apex_enabled=false
  if [[ -n ${XCIND_APP_APEX_URL_TEMPLATE:-} ]]; then
    apex_enabled=true
  fi

  # Parse all export entries and group by compose service
  local -a export_names=()
  local -a compose_services=()
  local -a ports=()
  local -a hostnames=()
  local -a routers_http=()
  local -a routers_https=()
  local -a export_tls=() # one of: both | https | http
  local apex_hostname=""
  local apex_router_http=""
  local apex_router_https=""
  local apex_tls="" # both | https | http — derived from primary export

  local entry
  local is_first_proxied=true
  for entry in "${XCIND_PROXY_EXPORTS[@]}"; do
    local _export_name _compose_service _port _type _tls _effective_tls
    __xcind-proxy-parse-entry "$entry" || return 1

    # This hook only handles proxied entries. Assigned entries flow through
    # xcind-assigned-hook, which reads the same array.
    [[ $_type == "proxied" ]] || continue

    # Validate service exists
    __xcind-proxy-validate-service "$_compose_service" "$resolved_config" || return 1

    # Resolve port
    if [ -z "$_port" ]; then
      _port=$(__xcind-proxy-infer-port "$_compose_service" "$resolved_config") || return 1
    fi

    # Resolve effective TLS for this export
    __xcind-proxy-resolve-export-tls "$proxy_tls_mode"

    # Generate hostname
    local hostname
    hostname=$(__xcind-render-template "$XCIND_APP_URL_TEMPLATE" \
      workspace "${XCIND_WORKSPACE:-}" app "$XCIND_APP" \
      export "$_export_name" domain "$XCIND_PROXY_DOMAIN")

    # Generate router names (http + https — the literal suffix is part of
    # the name, not the entrypoint; see ADR-0008 router naming convention).
    local router_http router_https
    router_http=$(__xcind-render-template "$XCIND_ROUTER_TEMPLATE" \
      workspace "${XCIND_WORKSPACE:-}" app "$XCIND_APP" \
      export "$_export_name" protocol "http")
    router_https=$(__xcind-render-template "$XCIND_ROUTER_TEMPLATE" \
      workspace "${XCIND_WORKSPACE:-}" app "$XCIND_APP" \
      export "$_export_name" protocol "https")

    # Generate apex hostname and routers for the first proxied entry only.
    # Order within the unified array still selects the apex anchor, but an
    # assigned entry placed earlier does not consume the apex slot.
    if [[ $is_first_proxied == true && $apex_enabled == true ]]; then
      apex_hostname=$(__xcind-render-template "$XCIND_APP_APEX_URL_TEMPLATE" \
        workspace "${XCIND_WORKSPACE:-}" app "$XCIND_APP" \
        domain "$XCIND_PROXY_DOMAIN")
      apex_router_http=$(__xcind-render-template "$XCIND_APEX_ROUTER_TEMPLATE" \
        workspace "${XCIND_WORKSPACE:-}" app "$XCIND_APP" \
        protocol "http")
      apex_router_https=$(__xcind-render-template "$XCIND_APEX_ROUTER_TEMPLATE" \
        workspace "${XCIND_WORKSPACE:-}" app "$XCIND_APP" \
        protocol "https")
      apex_tls="$_effective_tls"
    fi
    is_first_proxied=false

    export_names+=("$_export_name")
    compose_services+=("$_compose_service")
    ports+=("$_port")
    hostnames+=("$hostname")
    routers_http+=("$router_http")
    routers_https+=("$router_https")
    export_tls+=("$_effective_tls")
  done

  # Nothing proxied after filtering: no compose overlay, no -f flag.
  [[ ${#export_names[@]} -gt 0 ]] || return 0

  # Determine whether any export (incl. apex) needs the redirect middleware.
  local needs_redirect_middleware=false
  local t
  for t in "${export_tls[@]}"; do
    if [[ $t == "https" ]]; then
      needs_redirect_middleware=true
      break
    fi
  done

  # Build output grouped by compose service
  local output="services:"
  local -a seen_services=()

  local i=0
  while [ "$i" -lt "${#export_names[@]}" ]; do
    local svc="${compose_services[$i]}"

    # Check if we already rendered this compose service
    local already_seen=false
    local s
    for s in "${seen_services[@]+"${seen_services[@]}"}"; do
      if [ "$s" = "$svc" ]; then
        already_seen=true
        break
      fi
    done

    if [ "$already_seen" = true ]; then
      i=$((i + 1))
      continue
    fi

    seen_services+=("$svc")

    # Open the service block (networks + shared label prelude).
    local service_block
    service_block=$(__xcind-render-template "$XCIND_PROXY_SERVICE_HEADER_TEMPLATE" \
      compose_service "$svc")

    # Emit the shared redirect middleware on every rendered service block
    # when at least one export uses tls=require. Traefik's Docker provider
    # only discovers labels from running containers, so emitting this on a
    # single "first" service can leave other independently-started services
    # without the middleware they reference. Repeated definitions with the
    # same name/value are idempotent in Traefik.
    if [[ $needs_redirect_middleware == true ]]; then
      service_block+=$'\n'"$XCIND_PROXY_REDIRECT_MIDDLEWARE_LABELS"
    fi

    # Walk every export targeting this compose service, appending router +
    # URL labels for each. The first export on the service is also where the
    # apex routers/labels land (when it is index 0 of the overall list).
    local j=0
    while [ "$j" -lt "${#export_names[@]}" ]; do
      if [ "${compose_services[$j]}" = "$svc" ]; then
        local e_router_http="${routers_http[$j]}"
        local e_router_https="${routers_https[$j]}"
        local e_hostname="${hostnames[$j]}"
        local e_port="${ports[$j]}"
        local e_export="${export_names[$j]}"
        local e_tls="${export_tls[$j]}"

        local fragment
        case "$e_tls" in
        http)
          fragment=$(__xcind-render-template "$XCIND_PROXY_HTTP_ROUTER_TEMPLATE" \
            router "$e_router_http" hostname "$e_hostname" port "$e_port")
          service_block+=$'\n'"$fragment"
          ;;
        both)
          fragment=$(__xcind-render-template "$XCIND_PROXY_HTTP_ROUTER_TEMPLATE" \
            router "$e_router_http" hostname "$e_hostname" port "$e_port")
          service_block+=$'\n'"$fragment"
          fragment=$(__xcind-render-template "$XCIND_PROXY_HTTPS_ROUTER_TEMPLATE" \
            router_https "$e_router_https" hostname "$e_hostname" port "$e_port")
          service_block+=$'\n'"$fragment"
          ;;
        https)
          fragment=$(__xcind-render-template "$XCIND_PROXY_HTTP_REDIRECT_ROUTER_TEMPLATE" \
            router "$e_router_http" hostname "$e_hostname")
          service_block+=$'\n'"$fragment"
          fragment=$(__xcind-render-template "$XCIND_PROXY_HTTPS_ROUTER_TEMPLATE" \
            router_https "$e_router_https" hostname "$e_hostname" port "$e_port")
          service_block+=$'\n'"$fragment"
          ;;
        esac

        # Apex routers — rendered once, on the first export (index 0 overall)
        if [[ $j -eq 0 && $apex_enabled == true ]]; then
          case "$apex_tls" in
          http)
            fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HTTP_ROUTER_TEMPLATE" \
              apex_router "$apex_router_http" apex_hostname "$apex_hostname" port "$e_port")
            service_block+=$'\n'"$fragment"
            ;;
          both)
            fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HTTP_ROUTER_TEMPLATE" \
              apex_router "$apex_router_http" apex_hostname "$apex_hostname" port "$e_port")
            service_block+=$'\n'"$fragment"
            fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HTTPS_ROUTER_TEMPLATE" \
              apex_router_https "$apex_router_https" apex_hostname "$apex_hostname" port "$e_port")
            service_block+=$'\n'"$fragment"
            ;;
          https)
            fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HTTP_REDIRECT_ROUTER_TEMPLATE" \
              apex_router "$apex_router_http" apex_hostname "$apex_hostname")
            service_block+=$'\n'"$fragment"
            fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HTTPS_ROUTER_TEMPLATE" \
              apex_router_https "$apex_router_https" apex_hostname "$apex_hostname" port "$e_port")
            service_block+=$'\n'"$fragment"
            ;;
          esac
        fi

        # xcind.export.* host/url labels — one set per export.
        # `.http.url` is emitted whenever the export has an HTTP router
        # (always, for proxied exports — redirect-only counts) so consumers
        # see a stable HTTP entry point. `.https.url` is emitted only when
        # an HTTPS router actually exists. `.url` is the preferred-scheme
        # canonical (https when TLS is reachable, otherwise http).
        local preferred_scheme="http"
        [[ $e_tls == "both" || $e_tls == "https" ]] && preferred_scheme="https"
        fragment=$(__xcind-render-template "$XCIND_PROXY_EXPORT_HOST_LABEL" \
          export "$e_export" hostname "$e_hostname")
        service_block+=$'\n'"$fragment"
        fragment=$(__xcind-render-template "$XCIND_PROXY_EXPORT_HTTP_URL_LABEL" \
          export "$e_export" hostname "$e_hostname")
        service_block+=$'\n'"$fragment"
        if [[ $e_tls == "https" || $e_tls == "both" ]]; then
          fragment=$(__xcind-render-template "$XCIND_PROXY_EXPORT_HTTPS_URL_LABEL" \
            export "$e_export" hostname "$e_hostname")
          service_block+=$'\n'"$fragment"
        fi
        fragment=$(__xcind-render-template "$XCIND_PROXY_EXPORT_URL_LABEL" \
          export "$e_export" hostname "$e_hostname" scheme "$preferred_scheme")
        service_block+=$'\n'"$fragment"

        # xcind.apex.* host/url labels — rendered once on the first export.
        # Symmetric with the per-export labels above: `.http.url` is always
        # emitted (an HTTP apex router always exists, even as a redirect),
        # `.https.url` only when an HTTPS apex router exists.
        if [[ $j -eq 0 && $apex_enabled == true ]]; then
          local apex_preferred_scheme="http"
          [[ $apex_tls == "both" || $apex_tls == "https" ]] && apex_preferred_scheme="https"
          fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HOST_LABEL" \
            apex_hostname "$apex_hostname")
          service_block+=$'\n'"$fragment"
          fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HTTP_URL_LABEL" \
            apex_hostname "$apex_hostname")
          service_block+=$'\n'"$fragment"
          if [[ $apex_tls == "https" || $apex_tls == "both" ]]; then
            fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_HTTPS_URL_LABEL" \
              apex_hostname "$apex_hostname")
            service_block+=$'\n'"$fragment"
          fi
          fragment=$(__xcind-render-template "$XCIND_PROXY_APEX_URL_LABEL" \
            apex_hostname "$apex_hostname" scheme "$apex_preferred_scheme")
          service_block+=$'\n'"$fragment"
        fi
      fi
      j=$((j + 1))
    done

    output+=$'\n\n'"$service_block"
    i=$((i + 1))
  done

  # Append network footer
  output+=$'\n\nnetworks:\n  xcind-proxy:\n    external: true\n'

  # Write to generated dir
  echo "$output" >"$XCIND_GENERATED_DIR/compose.proxy.yaml"

  # Print compose flag to stdout
  echo "-f $XCIND_GENERATED_DIR/compose.proxy.yaml"
}

