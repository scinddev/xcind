#!/usr/bin/env bash
# xcind-proxy-lib.bash — Proxy hooks and shared lifecycle functions
#
# Provides two hooks:
#   xcind-proxy-hook (GENERATE) — generates Traefik proxy configuration
#   __xcind-proxy-execute-hook (EXECUTE) — ensures proxy is running
#
# Also provides __xcind-proxy-ensure-* functions used by both the hooks
# and the xcind-proxy CLI to manage proxy infrastructure.
#
# This file is auto-sourced by xcind-lib.bash. Hooks are registered by
# default — apps only need to declare XCIND_PROXY_EXPORTS to use them.

# --------------------------------------------------------------------------
# Shared Constants
# --------------------------------------------------------------------------

XCIND_PROXY_CONFIG_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/xcind/proxy"
XCIND_PROXY_STATE_DIR="${XDG_STATE_HOME:-$HOME/.local/state}/xcind/proxy"
XCIND_PROXY_COMPOSE="${XCIND_PROXY_STATE_DIR}/compose.yaml"
XCIND_PROXY_NETWORK="xcind-proxy"
# Backward compat alias for messages and hook references
# shellcheck disable=SC2034 # Used externally by tests and CLI
XCIND_PROXY_DIR="$XCIND_PROXY_CONFIG_DIR"

# --------------------------------------------------------------------------
# Proxy Lifecycle Functions
# --------------------------------------------------------------------------

# Ensure the xcind-proxy Docker network exists.
__xcind-proxy-ensure-network() {
  if docker network inspect "$XCIND_PROXY_NETWORK" &>/dev/null; then
    return 0
  fi

  if ! docker network create "$XCIND_PROXY_NETWORK" >/dev/null; then
    echo "Error: Failed to create Docker network '$XCIND_PROXY_NETWORK'." >&2
    echo "Check that Docker is running and you have permission to create networks." >&2
    return 1
  fi
}

# Write (or overwrite) config.sh with the current XCIND_PROXY_* values.
# Callers must set the variables before calling this function.
__xcind-proxy-write-config() {
  cat >"$XCIND_PROXY_CONFIG_DIR/config.sh" <<EOF
# xcind-proxy global configuration
# This file is regenerated by \`xcind-proxy init\` when flags are passed.

# Domain suffix for generated hostnames (.localhost = zero DNS config, RFC 6761)
XCIND_PROXY_DOMAIN="${XCIND_PROXY_DOMAIN:-localhost}"

# Traefik Docker image
XCIND_PROXY_IMAGE="${XCIND_PROXY_IMAGE:-traefik:v3}"

# Host port for HTTP traffic
XCIND_PROXY_HTTP_PORT="${XCIND_PROXY_HTTP_PORT:-80}"

# Enable Traefik dashboard
XCIND_PROXY_DASHBOARD="${XCIND_PROXY_DASHBOARD:-false}"

# Dashboard port (only used if dashboard is enabled)
XCIND_PROXY_DASHBOARD_PORT="${XCIND_PROXY_DASHBOARD_PORT:-8080}"

# TLS mode: auto | custom | disabled
#   auto     — mkcert wildcard for \$XCIND_PROXY_DOMAIN if mkcert is available;
#              otherwise a self-signed wildcard generated via openssl.
#   custom   — use XCIND_PROXY_TLS_CERT_FILE / XCIND_PROXY_TLS_KEY_FILE.
#   disabled — HTTP-only, no :443 entrypoint, no certs.
XCIND_PROXY_TLS_MODE="${XCIND_PROXY_TLS_MODE:-auto}"

# Host port for HTTPS traffic (ignored when TLS is disabled)
XCIND_PROXY_HTTPS_PORT="${XCIND_PROXY_HTTPS_PORT:-443}"

# Custom TLS cert/key (absolute paths) — only used when TLS mode = custom
XCIND_PROXY_TLS_CERT_FILE="${XCIND_PROXY_TLS_CERT_FILE:-}"
XCIND_PROXY_TLS_KEY_FILE="${XCIND_PROXY_TLS_KEY_FILE:-}"
EOF
}

# Ensure proxy infrastructure files exist.
# Creates config.sh (if missing) in XCIND_PROXY_CONFIG_DIR,
# compose.yaml and traefik.yaml in XCIND_PROXY_STATE_DIR.
# Note: xcind-proxy init may regenerate config.sh explicitly via
# __xcind-proxy-write-config when CLI flags are passed.
# Calls __xcind-proxy-ensure-network after generating files.
__xcind-proxy-ensure-init() {
  mkdir -p "$XCIND_PROXY_CONFIG_DIR" "$XCIND_PROXY_STATE_DIR"

  # Write config.sh only if it doesn't exist (preserves user edits)
  if [ ! -f "$XCIND_PROXY_CONFIG_DIR/config.sh" ]; then
    __xcind-proxy-write-config
  fi

  # Source config for variable expansion
  # shellcheck disable=SC1091
  source "$XCIND_PROXY_CONFIG_DIR/config.sh"

  local tls_mode="${XCIND_PROXY_TLS_MODE:-auto}"
  local tls_enabled=true
  [[ $tls_mode == "disabled" ]] && tls_enabled=false

  # Dashboard bits (orthogonal to TLS)
  local dashboard_command=""
  local dashboard_port=""
  if [ "${XCIND_PROXY_DASHBOARD:-false}" = "true" ]; then
    dashboard_command='
      - "--api.dashboard=true"'
    dashboard_port="
      - \"${XCIND_PROXY_DASHBOARD_PORT:-8080}:8080\""
  fi

  # TLS-conditional compose fragments
  local https_port_line=""
  local cert_volume_line=""
  local dynamic_volume_line=""
  if [[ $tls_enabled == true ]]; then
    https_port_line="
      - \"${XCIND_PROXY_HTTPS_PORT:-443}:443\""
    cert_volume_line="
      - ./certs:/etc/traefik/certs:ro"
    dynamic_volume_line="
      - ./dynamic:/etc/traefik/dynamic:ro"
  fi

  cat >"$XCIND_PROXY_COMPOSE" <<EOF
name: xcind-proxy

services:
  traefik:
    image: ${XCIND_PROXY_IMAGE:-traefik:v3}
    command:
      - "--configFile=/etc/traefik/traefik.yaml"${dashboard_command}
    ports:
      - "${XCIND_PROXY_HTTP_PORT:-80}:80"${https_port_line}${dashboard_port}
    volumes:
      - /var/run/docker.sock:/var/run/docker.sock:ro
      - ./traefik.yaml:/etc/traefik/traefik.yaml:ro${dynamic_volume_line}${cert_volume_line}
    networks:
      - xcind-proxy
    restart: unless-stopped
    labels:
      - "xcind.managed=true"
      - "xcind.component=proxy"

networks:
  xcind-proxy:
    external: true
EOF

  # Write traefik.yaml (always regenerated)
  local dashboard_config=""
  if [ "${XCIND_PROXY_DASHBOARD:-false}" = "true" ]; then
    dashboard_config="
api:
  dashboard: true
  insecure: true"
  fi

  # TLS-conditional traefik.yaml fragments
  local websecure_entry=""
  local file_provider=""
  if [[ $tls_enabled == true ]]; then
    websecure_entry="
  websecure:
    address: \":443\""
    file_provider="
  file:
    directory: /etc/traefik/dynamic
    watch: true"
  fi

  cat >"$XCIND_PROXY_STATE_DIR/traefik.yaml" <<EOF
entryPoints:
  web:
    address: ":80"${websecure_entry}

providers:
  docker:
    exposedByDefault: false
    network: xcind-proxy${file_provider}

log:
  level: INFO${dashboard_config}
EOF

  # Dynamic TLS config (only when TLS is enabled)
  local dynamic_dir="$XCIND_PROXY_STATE_DIR/dynamic"
  if [[ $tls_enabled == true ]]; then
    mkdir -p "$dynamic_dir"
    cat >"$dynamic_dir/tls.yaml" <<'EOF'
# AUTO-GENERATED by xcind-proxy. Regenerated on each init/up.
tls:
  certificates:
    - certFile: /etc/traefik/certs/wildcard.crt
      keyFile: /etc/traefik/certs/wildcard.key
  stores:
    default:
      defaultCertificate:
        certFile: /etc/traefik/certs/wildcard.crt
        keyFile: /etc/traefik/certs/wildcard.key
EOF
    # Ensure the certs directory exists so the bind mount doesn't fail
    # even when cert provisioning is deferred to __xcind-proxy-ensure-certs.
    mkdir -p "$XCIND_PROXY_STATE_DIR/certs"
  else
    # TLS disabled: remove any stale dynamic/ tls config to keep Traefik happy.
    rm -f "$dynamic_dir/tls.yaml"
  fi

  # Migration: remove generated files from old locations.
  # - Config dir: the previous config-dir → state-dir migration.
  # - State dir/docker-compose.yaml: the previous docker-compose.yaml →
  #   compose.yaml rename (Compose Specification v1.28+ convention).
  rm -f \
    "$XCIND_PROXY_CONFIG_DIR/docker-compose.yaml" \
    "$XCIND_PROXY_CONFIG_DIR/traefik.yaml" \
    "$XCIND_PROXY_STATE_DIR/docker-compose.yaml"

  # Create Docker network if it doesn't exist
  __xcind-proxy-ensure-network
}

# Provision the wildcard TLS certificate used by Traefik when TLS is enabled.
# Writes $XCIND_PROXY_STATE_DIR/certs/wildcard.{crt,key} and a sibling
# "domain" marker file recording the domain the cert was minted for.
#
# Resolution order (auto mode):
#   1. user-provided certs at $XCIND_PROXY_CONFIG_DIR/certs/wildcard.{crt,key}
#      (always wins — copied into the state cert when newer)
#   2. previously generated state cert for the same domain (fast path)
#   3. mkcert (if on PATH) → mints a locally-trusted wildcard
#   4. openssl (if on PATH) → self-signed wildcard fallback
#   5. error with installation guidance
#
# Custom mode copies the configured XCIND_PROXY_TLS_CERT_FILE / KEY_FILE.
# Disabled mode is a no-op.
#
# Returns 0 on success (or when TLS is disabled), non-zero on failure.
__xcind-proxy-ensure-certs() {
  local mode="${XCIND_PROXY_TLS_MODE:-auto}"
  [[ $mode == "disabled" ]] && return 0

  local domain="${XCIND_PROXY_DOMAIN:-localhost}"
  local certs_dir="$XCIND_PROXY_STATE_DIR/certs"
  local cert="$certs_dir/wildcard.crt"
  local key="$certs_dir/wildcard.key"
  local marker="$certs_dir/domain"

  mkdir -p "$certs_dir"

  # Regenerate if the cert was minted for a different domain.
  if [[ -f $marker ]]; then
    local recorded
    recorded=$(cat "$marker" 2>/dev/null || true)
    if [[ $recorded != "$domain" ]]; then
      rm -f "$cert" "$key"
    fi
  fi

  if [[ $mode == "custom" ]]; then
    if [[ -z ${XCIND_PROXY_TLS_CERT_FILE:-} || -z ${XCIND_PROXY_TLS_KEY_FILE:-} ]]; then
      echo "xcind-proxy: TLS mode is 'custom' but XCIND_PROXY_TLS_CERT_FILE / XCIND_PROXY_TLS_KEY_FILE are not set." >&2
      return 1
    fi
    if [[ ! -r $XCIND_PROXY_TLS_CERT_FILE ]]; then
      echo "xcind-proxy: cannot read cert file: $XCIND_PROXY_TLS_CERT_FILE" >&2
      return 1
    fi
    if [[ ! -r $XCIND_PROXY_TLS_KEY_FILE ]]; then
      echo "xcind-proxy: cannot read key file: $XCIND_PROXY_TLS_KEY_FILE" >&2
      return 1
    fi
    cp "$XCIND_PROXY_TLS_CERT_FILE" "$cert"
    cp "$XCIND_PROXY_TLS_KEY_FILE" "$key"
    chmod 600 "$key"
    printf '%s\n' "$domain" >"$marker"
    return 0
  fi

  # auto mode — check user-provided wildcard first (developer-curated
  # certs always win over previously-generated state), then fall back to
  # the state cache, then generate via mkcert / openssl.

  # 1. User-provided wildcard in config dir
  local user_cert="$XCIND_PROXY_CONFIG_DIR/certs/wildcard.crt"
  local user_key="$XCIND_PROXY_CONFIG_DIR/certs/wildcard.key"
  if [[ -r $user_cert && -r $user_key ]]; then
    # Refresh only when the user files changed (or we haven't copied yet)
    # so we don't rewrite the state cert on every `xcind-proxy up`.
    if [[ ! -s $cert || ! -s $key || $user_cert -nt $cert || $user_key -nt $key ]]; then
      cp "$user_cert" "$cert"
      cp "$user_key" "$key"
      chmod 600 "$key"
      echo "xcind-proxy: installed TLS cert from $XCIND_PROXY_CONFIG_DIR/certs/" >&2
    fi
    printf '%s\n' "$domain" >"$marker"
    return 0
  fi

  # 2. Reuse previously generated state cert for this domain
  if [[ -s $cert && -s $key ]]; then
    printf '%s\n' "$domain" >"$marker"
    return 0
  fi

  # 3. mkcert (preferred — produces locally-trusted cert)
  if command -v mkcert >/dev/null 2>&1; then
    echo "xcind-proxy: minting wildcard cert for *.${domain} via mkcert..." >&2
    if mkcert -cert-file "$cert" -key-file "$key" "*.${domain}" "${domain}" >&2; then
      chmod 600 "$key"
      printf '%s\n' "$domain" >"$marker"
      return 0
    fi
    echo "xcind-proxy: mkcert failed; falling back to self-signed." >&2
  fi

  # 4. openssl self-signed wildcard fallback
  if command -v openssl >/dev/null 2>&1; then
    echo "xcind-proxy: generating self-signed wildcard cert for *.${domain} via openssl..." >&2
    local cnf
    cnf=$(mktemp)
    cat >"$cnf" <<EOF
[req]
distinguished_name = req_dn
x509_extensions = v3_ext
prompt = no
[req_dn]
CN = *.${domain}
O = xcind
[v3_ext]
subjectAltName = DNS:*.${domain},DNS:${domain}
basicConstraints = CA:FALSE
keyUsage = digitalSignature,keyEncipherment
extendedKeyUsage = serverAuth
EOF
    if openssl req -x509 -nodes -newkey rsa:2048 -days 825 \
      -keyout "$key" -out "$cert" -config "$cnf" >/dev/null 2>&1; then
      chmod 600 "$key"
      printf '%s\n' "$domain" >"$marker"
      rm -f "$cnf"
      return 0
    fi
    rm -f "$cnf"
    echo "xcind-proxy: openssl failed to generate self-signed cert." >&2
  fi

  echo "xcind-proxy: unable to provision a TLS certificate — install 'mkcert' or 'openssl'," >&2
  echo "             place cert/key at $user_cert and $user_key," >&2
  echo "             or set XCIND_PROXY_TLS_MODE=disabled." >&2
  return 1
}

# Ensure the proxy is fully operational: initialized, network exists, Traefik running.
# Skips auto-start if XCIND_PROXY_AUTO_START=0.
# All output goes to stderr to avoid interfering with hook stdout.
__xcind-proxy-ensure-running() {
  # Opt-out: skip auto-start entirely
  if [[ ${XCIND_PROXY_AUTO_START:-1} == "0" ]]; then
    # Still ensure network exists for compose overlay compatibility
    __xcind-proxy-ensure-network 2>/dev/null || true
    return 0
  fi

  # Fast path: check if proxy is already running
  local running
  running=$(docker ps --filter label=xcind.component=proxy --filter status=running -q 2>/dev/null) || true
  if [ -n "$running" ]; then
    # Warn if config.sh is newer than generated files (stale)
    if [[ -f "$XCIND_PROXY_CONFIG_DIR/config.sh" && -f $XCIND_PROXY_COMPOSE ]] &&
      [[ "$XCIND_PROXY_CONFIG_DIR/config.sh" -nt $XCIND_PROXY_COMPOSE ]]; then
      echo "xcind-proxy: config.sh changed; run 'xcind-proxy up' to apply." >&2
    fi
    return 0
  fi

  # Ensure init has been done
  if [ ! -f "$XCIND_PROXY_COMPOSE" ]; then
    echo "xcind-proxy: Initializing proxy config at $XCIND_PROXY_CONFIG_DIR" >&2
  fi
  __xcind-proxy-ensure-init

  # Provision TLS certs (no-op when TLS mode = disabled).
  __xcind-proxy-ensure-certs || return $?

  # Start Traefik
  echo "xcind-proxy: Starting Traefik proxy..." >&2
  if ! docker compose -f "$XCIND_PROXY_COMPOSE" up -d 1>&2; then
    echo "xcind-proxy: Failed to start Traefik. Check 'xcind-proxy status' for details." >&2
    echo "xcind-proxy: Port ${XCIND_PROXY_HTTP_PORT:-80} may already be in use." >&2
    # Non-fatal: the compose overlay is still generated with correct labels
  fi
}

